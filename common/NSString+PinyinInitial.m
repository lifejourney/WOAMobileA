@implementation NSString (PinyinInitial)

- (NSString*) pinyinInitials
{
    NSString *initialsString = @"";
    unichar character;
    NSInteger tableIndex;
    unichar initial;
    
    for (NSUInteger index = 0; index < [self length]; index++)
    {
        character = [self characterAtIndex: index];
        tableIndex = character - kPinyinUnicodeIndexStart;
        
        if (tableIndex >= 0 && tableIndex < kPinyinCharacterCount)
        {
            initial = pingyinInitialCharacterArray[tableIndex];
            initialsString = [initialsString stringByAppendingFormat: @"%C", initial];
        }
        else if (character >= 'A' && character <= 'Z')
            initialsString = [initialsString stringByAppendingFormat: @"%C", character];
        else if (character >= 'a' && character <= 'z')
            initialsString = [initialsString stringByAppendingFormat: @"%C", (unichar)(character - 32)];
        else
            initialsString = [initialsString stringByAppendingString: @"?"];
    }
    
    return initialsString;
}

